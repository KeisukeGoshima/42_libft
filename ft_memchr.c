#include "libft.h"

void	*ft_memchr(const void *buf, int ch, size_t n)
{
	char	*str;
	size_t	i;

	str = (char *)buf;
	ch %= 256;
	i = 0;
	while (i < n)
	{
		if (str[i] == ch)
			return ((void *)&str[i]);
		i++;
	}
	return (NULL);
}
